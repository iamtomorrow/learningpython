#Nested Dictionaries

childA = {
        "name": "Robert",
        "year": 2009
    },
childB = {
        "name": "George",
        "year": 2010
    },
childC = {
        "name": "Emily",
        "year": 2013
    }

ourFamily = {
    "ChildA": childA,
    "ChildB": childB,
    "ChildC": childC
}
print(ourFamily)
